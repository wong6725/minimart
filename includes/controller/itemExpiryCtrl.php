	public function view_form( $id = 0, $templating = true, $isView = false )
	{
		$args = array(
			'setting'	=> $this->setting,
			'section'	=> $this->section_id,
			'hook'		=> $this->section_id.'_form',
			'action'	=> 'save',
			'token'		=> apply_filters( 'wcwh_generate_token', $this->section_id ),
			'new'		=> 'new',
		);
		if( $this->warehouse['id'] && $this->view_outlet ) $args['seller'] = $this->warehouse['id'];

		if( $id )
		{
			$filters = [ 'id' => $id ];
			if( $this->warehouse['id'] && $this->view_outlet ) $filters['seller'] = $this->warehouse['id'];
			
			$datas = $this->Logic->get_infos( $filters, [], true, [ ] );
			if( $datas )
			{
				$args['action'] = 'update';
				if( $isView ) $args['view'] = true;

				$args['scheme'] = $datas['scheme'];
				switch( $args['scheme'] )
				{
					case 'item_category':
						$args['tplName'] = $this->tplName['newCategory'];
					break;
					case 'item':
						$args['tplName'] = $this->tplName['newItem'];
					break;
				}
				$args['data'] = $datas;
				unset( $args['new'] );
			}
		}

		do_action( 'wcwh_get_template', 'form/itemExpiry-form.php', $args );
	}

