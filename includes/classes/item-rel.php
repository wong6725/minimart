	public function set_db_tables()
	{
		global $wpdb, $wcwh;
		$prefix = $this->get_prefix();

		$this->tables = array(
			"main" 			=> $prefix.$this->tbl,
			
			"items"			=> $prefix."items",
			"itemstree"		=> $prefix."items_tree",
			"itemsmeta"		=> $prefix."itemsmeta",
			"uom"			=> $prefix."uom",
