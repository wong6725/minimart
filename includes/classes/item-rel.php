class WCWH_ItemRel_Class extends WCWH_CRUD_Controller 
{
	protected $section_id = "wh_items_relation";

	protected $tbl = "item_relation";

	protected $primary_key = "id";

	protected $tables = array();

	public $Notices;
	public $className = "ItemRel_Class";

	public $update_tree_child = true;
	public $one_step_delete = false;
	public $true_delete = false;
	public $useFlag = false;

	protected $warehouse = array();

	public function __construct( $db_wpdb = array() )
	{
		parent::__construct();

		if( $db_wpdb ) $this->db_wpdb = $db_wpdb;

